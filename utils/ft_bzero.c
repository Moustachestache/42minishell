#include "../minishell.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	l;
	char	*buffer;

	l = 0;
	buffer = (char *) s;
	while (l < n)
	{
		buffer[l] = 0;
		l++;
	}
}
